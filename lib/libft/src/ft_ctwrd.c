#include "libft.h"

int	ft_ctwrd(const char *s, char c)
{	
	int count;

	count = 0;
	while(*s)
	{
		if (*s == c)
			s++;
		else
		{
			while (*s && *s != c)
				s++;
			count++;
		}
	}
	return (count);
}
